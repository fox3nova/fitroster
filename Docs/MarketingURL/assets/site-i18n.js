  function normalizeLanguage(language) {
    const value = String(language || "").toLowerCase();
    if (!value) {
      return "";
    }
    if (value === "zh-hans" || value === "zh-cn" || value === "zh-sg" || value.startsWith("zh-hans")) {
      return "zh-Hans";
    }
    if (value === "zh-hant" || value === "zh-tw" || value === "zh-hk" || value === "zh-mo" || value.startsWith("zh")) {
      return "zh-Hant";
    }
    if (value.startsWith("ja")) return "ja";
    if (value.startsWith("ko")) return "ko";
    if (value.startsWith("th")) return "th";
    if (value.startsWith("ms") || value.startsWith("ms-my")) return "ms";
    if (value.startsWith("ru")) return "ru";
    if (value.startsWith("de")) return "de";
    if (value.startsWith("fr")) return "fr";
    if (value.startsWith("es")) return "es";
    if (value.startsWith("en")) return "en";
    return "";
  }

  function preferredLanguage(supportedLanguages) {
    const requestedLanguage = normalizeLanguage(new URLSearchParams(window.location.search).get("lang")
      || new URLSearchParams(window.location.search).get("language")
      || new URLSearchParams(window.location.search).get("locale"));
    if (supportedLanguages.includes(requestedLanguage)) {
      return requestedLanguage;
    }

    const stored = localStorage.getItem("fitroster.marketing.language");
    const normalizedStored = normalizeLanguage(stored);
    if (supportedLanguages.includes(normalizedStored)) {
      return normalizedStored;
    }

    const browserLanguages = navigator.languages && navigator.languages.length
      ? navigator.languages
      : [navigator.language || ""];

    for (const browserLanguage of browserLanguages) {
      const normalized = normalizeLanguage(browserLanguage);
      if (supportedLanguages.includes(normalized)) {
        return normalized;
      }
    }
    return supportedLanguages.includes("en") ? "en" : supportedLanguages[0];
  }

  function extend(pageKey, translations) {
    Object.assign(translations, pages[pageKey] || {});
  }

  function mountLanguageSelect(supportedLanguages, setLanguage) {
    const select = document.querySelector("[data-language-select]");
    if (!select) {
      return;
    }

    select.replaceChildren();
    languageOptions
      .filter((option) => supportedLanguages.includes(option.code))
      .forEach((option) => {
        const element = document.createElement("option");
        element.value = option.code;
        element.textContent = option.label;
        select.appendChild(element);
      });

    select.addEventListener("change", () => setLanguage(select.value));
  }

  function updateLanguageSelect(language) {
    const select = document.querySelector("[data-language-select]");
    if (select) {
      select.value = language;
    }
  }

